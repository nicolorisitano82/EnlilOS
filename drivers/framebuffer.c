#define FONT_EXT_COUNT  ((uint32_t)(sizeof(font_ext) / sizeof(font_ext[0])))

/*
 * font_ext_lookup — ricerca binaria del glyph per codepoint Unicode.
 * Ritorna il glyph per cp se trovato, altrimenti il glyph di U+FFFD.
 * Mai NULL — U+FFFD è garantito essere l'ultimo entry della tabella.
 * WCET: O(log FONT_EXT_COUNT) = costante per tabella fissa.
 */
static const uint8_t *font_ext_lookup(uint32_t cp)
{
    uint32_t lo = 0, hi = FONT_EXT_COUNT;
    while (lo < hi) {
        uint32_t mid = lo + (hi - lo) / 2;
        if (font_ext[mid].cp == cp)
            return font_ext[mid].glyph;
        if (font_ext[mid].cp < cp)
            lo = mid + 1;
        else
            hi = mid;
    }
    /* Non trovato: U+FFFD è sempre l'ultimo entry */
    return font_ext[FONT_EXT_COUNT - 1U].glyph;
}

/*
 * fb_draw_char_utf8 — disegna un singolo codepoint Unicode.
 *
 * - ASCII (0x20–0x7E): usa font_8x16 direttamente O(1).
 * - Extended (>0x7E):  usa font_ext_lookup() O(log N), mai NULL.
 */
void fb_draw_char_utf8(uint32_t x, uint32_t y, uint32_t cp,
                       uint32_t fg, uint32_t bg)
{
    const uint8_t *glyph;

    if (cp >= 0x20U && cp <= 0x7EU) {
        glyph = font_8x16[(int)(cp - 0x20U)];
    } else if (cp == '\t' || cp == '\n' || cp == '\r') {
        return;
    } else {
        glyph = font_ext_lookup(cp);
    }

    for (uint32_t row = 0; row < 16; row++) {
        uint8_t bits = glyph[row];
        for (uint32_t col = 0; col < 8; col++) {
            uint32_t color = (bits & (0x80U >> col)) ? fg : bg;
            fb_put_pixel(x + col, y + row, color);
        }
    }
}

/*
 * fb_draw_string_utf8 — renderizza stringa UTF-8 a partire da (x, y).
 * Avanza di 8 pixel per ogni codepoint (font monospazio).
 */
void fb_draw_string_utf8(uint32_t x, uint32_t y, const char *s,
                         uint32_t fg, uint32_t bg)
{
    uint32_t cx = x;
    while (*s) {
        uint32_t cp = utf8_decode(&s);
        if (cp == 0) break;
        fb_draw_char_utf8(cx, y, cp, fg, bg);
        cx += 8;
    }
}

/*
 * fb_draw_string_centered_utf8 — renderizza stringa UTF-8 centrata.
 * Centra in base al numero di codepoint (non di byte).
 */
void fb_draw_string_centered_utf8(const char *s, uint32_t fg, uint32_t bg)
{
    uint32_t len = utf8_strlen(s);
    uint32_t text_w = len * 8;
    uint32_t x = (FB_WIDTH > text_w) ? (FB_WIDTH - text_w) / 2 : 0;
    uint32_t y = (FB_HEIGHT - 16) / 2;
    fb_draw_string_utf8(x, y, s, fg, bg);
}
