/* QEMU fw_cfg MMIO interface (virt machine) */
#define FW_CFG_BASE         0x09020000
#define FW_CFG_DATA         (FW_CFG_BASE + 0x00)  /* 8-bit data read/write */
#define FW_CFG_SELECTOR     (FW_CFG_BASE + 0x08)  /* 16-bit selector (BE) */
#define FW_CFG_DMA_ADDR     (FW_CFG_BASE + 0x10)  /* 64-bit DMA address (BE) */

/* fw_cfg well-known selectors */
#define FW_CFG_FILE_DIR     0x0019

/* fw_cfg DMA control bits */
#define FW_CFG_DMA_CTL_ERROR    0x01
#define FW_CFG_DMA_CTL_READ     0x02
#define FW_CFG_DMA_CTL_SKIP     0x04
#define FW_CFG_DMA_CTL_SELECT   0x08
#define FW_CFG_DMA_CTL_WRITE    0x10

/* ramfb config structure (28 bytes) */
typedef struct __attribute__((packed)) {
    uint64_t addr;
    uint32_t fourcc;
    uint32_t flags;
    uint32_t width;
    uint32_t height;
    uint32_t stride;
} ramfb_cfg_t;

/* fw_cfg file directory entry */
typedef struct __attribute__((packed)) {
    uint32_t size;
    uint16_t select;
    uint16_t reserved;
    char     name[56];
} fw_cfg_file_t;

/* Byte swap helpers per big-endian fw_cfg */
static uint32_t bswap32(uint32_t v) {
    return ((v & 0xFF) << 24) | ((v & 0xFF00) << 8) |
           ((v >> 8) & 0xFF00) | ((v >> 24) & 0xFF);
}

static uint64_t bswap64(uint64_t v) {
    return ((uint64_t)bswap32((uint32_t)v) << 32) |
           (uint64_t)bswap32((uint32_t)(v >> 32));
}

static uint16_t bswap16(uint16_t v) {
    return (v >> 8) | (v << 8);
}

static void fb_pr_hex32(uint32_t v)
{
    static const char h[] = "0123456789ABCDEF";
    uart_puts("0x");
    for (int s = 28; s >= 0; s -= 4)
        uart_putc(h[(v >> s) & 0xFU]);
}

static void fb_pr_hex64(uint64_t v)
{
    fb_pr_hex32((uint32_t)(v >> 32));
    for (int s = 28; s >= 0; s -= 4) {
        static const char h[] = "0123456789ABCDEF";
        uart_putc(h[((uint32_t)v >> s) & 0xFU]);
    }
}

/*
 * fw_cfg_dma_write — scrive 'len' byte dal buffer fisico 'data_pa'
 * nel file fw_cfg identificato da 'sel', usando l'interfaccia DMA.
 *
 * Questa è l'unica modalità di scrittura supportata in modo affidabile
 * su QEMU virt. La scrittura byte-per-byte al DATA register richiede
 * FW_CFG_WRITE_CHANNEL (0x4000) nel selettore, che non sempre funziona.
 *
 * Protocollo:
 *   1. Prepara QEMUCFGDMAAccess in RAM (buffer statico, allineato a 8B)
 *   2. DC CIVAC sul buffer dati  → QEMU DMA vede i nostri dati
 *   3. DC CIVAC sulla struct DMA → QEMU DMA vede la richiesta
 *   4. Scrivi PA della struct a FW_CFG_DMA_ADDR (64-bit big-endian)
 *   5. Poll: DC IVAC + leggi control fino a (control & ~ERROR) == 0
 */
typedef struct __attribute__((packed, aligned(8))) {
    uint32_t control;   /* BE: [31:16]=selector [4]=WRITE [3]=SELECT [1]=READ */
    uint32_t length;    /* BE: byte da trasferire */
    uint64_t address;   /* BE: indirizzo fisico del buffer dati */
} fw_cfg_dma_t;

static fw_cfg_dma_t  fw_dma_req;           /* deve stare in RAM, non stack */
static ramfb_cfg_t   fw_ramfb_cfg_buf;     /* idem */


static int fw_cfg_dma_submit(uint32_t control, void *data, uint32_t len)
{
    fw_dma_req.control = bswap32(control);
    fw_dma_req.length  = bswap32(len);
    fw_dma_req.address = bswap64((uint64_t)(uintptr_t)data);

    if (control & FW_CFG_DMA_CTL_WRITE)
        cache_flush_range((uintptr_t)data, len);

    cache_flush_range((uintptr_t)&fw_dma_req, sizeof(fw_dma_req));

    uint64_t req_pa = (uint64_t)(uintptr_t)&fw_dma_req;
    *(volatile uint64_t *)(FW_CFG_DMA_ADDR) = bswap64(req_pa);
    __asm__ volatile("dsb sy" ::: "memory");

    for (uint32_t retry = 0; retry < 100000U; retry++) {
        cache_invalidate_range((uintptr_t)&fw_dma_req, sizeof(fw_dma_req));

        uint32_t ctrl = bswap32(fw_dma_req.control);
        if ((ctrl & ~FW_CFG_DMA_CTL_ERROR) == 0U) {
            if (control & FW_CFG_DMA_CTL_READ)
                cache_invalidate_range((uintptr_t)data, len);

            if (ctrl & FW_CFG_DMA_CTL_ERROR) {
                uart_puts("[FB] fw_cfg DMA: errore segnalato da QEMU\n");
                return -1;
            }
            return 0;
        }
    }

    uart_puts("[FB] fw_cfg DMA: timeout\n");
    return -1;
}

static int fw_cfg_dma_read_sel(uint16_t sel, void *data, uint32_t len)
{
    return fw_cfg_dma_submit(((uint32_t)sel << 16) |
                             FW_CFG_DMA_CTL_SELECT |
                             FW_CFG_DMA_CTL_READ,
                             data, len);
}

static int fw_cfg_dma_read_next(void *data, uint32_t len)
{
    return fw_cfg_dma_submit(FW_CFG_DMA_CTL_READ, data, len);
}

static int fw_cfg_dma_write_sel(uint16_t sel, const void *data, uint32_t len)
{
    /*
     * fw_cfg DMA legge/scrive verso RAM guest, quindi possiamo passare il
     * puntatore diretto a un buffer statico/copia già residente in RAM.
     */
    return fw_cfg_dma_submit(((uint32_t)sel << 16) |
                             FW_CFG_DMA_CTL_SELECT |
                             FW_CFG_DMA_CTL_WRITE,
                             (void *)(uintptr_t)data, len);
}

static void fw_cfg_read_signature(char sig[5])
{
    uint32_t raw = 0U;

    if (fw_cfg_dma_read_sel(0x0000U, &raw, sizeof(raw)) != 0) {
        sig[0] = '?';
        sig[1] = '?';
        sig[2] = '?';
        sig[3] = '?';
        sig[4] = '\0';
        return;
    }

    sig[0] = (char)((raw >> 24) & 0xFFU);
    sig[1] = (char)((raw >> 16) & 0xFFU);
    sig[2] = (char)((raw >>  8) & 0xFFU);
    sig[3] = (char)( raw        & 0xFFU);
    sig[4] = '\0';
}

static uint32_t fw_cfg_read_id(void)
{
    uint32_t raw = 0U;
    if (fw_cfg_dma_read_sel(0x0001U, &raw, sizeof(raw)) != 0)
        return 0U;
    return bswap32(raw);
}

static uint64_t fw_cfg_dma_signature(void)
{
    return bswap64(*(volatile uint64_t *)(FW_CFG_DMA_ADDR));
}

static void fw_cfg_dma_write(uint16_t sel, const void *data, uint32_t len)
{
    /* Copia dati nel buffer statico in RAM */
    const uint8_t *src = (const uint8_t *)data;
    uint8_t       *dst = (uint8_t *)&fw_ramfb_cfg_buf;
    for (uint32_t i = 0; i < len; i++) dst[i] = src[i];
    (void)fw_cfg_dma_write_sel(sel, &fw_ramfb_cfg_buf, len);
}

/* Confronto stringhe inline */
static int fw_strcmp(const char *a, const char *b)
{
    while (*a && *a == *b) { a++; b++; }
    return *(const uint8_t *)a - *(const uint8_t *)b;
}

/*
 * Cerca un file nel fw_cfg directory e ritorna il suo selector.
 * Ritorna 0 se non trovato.
 */
static uint16_t fw_cfg_find_file(const char *name)
{
    /* Il primo uint32_t (BE) è il numero di file */
    uint32_t count_be = 0U;
    if (fw_cfg_dma_read_sel(FW_CFG_FILE_DIR, &count_be, sizeof(count_be)) != 0) {
        uart_puts("[EnlilOS] fw_cfg: impossibile leggere la directory\n");
        return 0;
    }
    uint32_t count = bswap32(count_be);

    uart_puts("[EnlilOS] fw_cfg: ");
    /* Stampa count come decimale semplice */
    char num[12];
    int pos = 0;
    uint32_t tmp = count;
    if (tmp == 0) { num[pos++] = '0'; }
    else {
        char rev[12];
        int rp = 0;
        while (tmp > 0) { rev[rp++] = '0' + (tmp % 10); tmp /= 10; }
        while (rp > 0) { num[pos++] = rev[--rp]; }
    }
    num[pos] = '\0';
    uart_puts(num);
    uart_puts(" file nel directory\n");

    for (uint32_t i = 0; i < count; i++) {
        fw_cfg_file_t entry;
        if (fw_cfg_dma_read_next(&entry, sizeof(fw_cfg_file_t)) != 0) {
            uart_puts("[EnlilOS] fw_cfg: errore leggendo una entry\n");
            return 0;
        }

        if (fw_strcmp(entry.name, name) == 0) {
            uint16_t sel = bswap16(entry.select);
            uart_puts("[EnlilOS] fw_cfg: trovato '");
            uart_puts(name);
            uart_puts("'\n");
            return sel;
        }
    }

    uart_puts("[EnlilOS] fw_cfg: '");
    uart_puts(name);
    uart_puts("' NON trovato\n");
    return 0;
}

void fb_init(void)
{
    fb_ptr = framebuffer;

    char sig[5];
    uint32_t fw_id;
    uint64_t dma_sig;

    fw_cfg_read_signature(sig);
    fw_id = fw_cfg_read_id();
    dma_sig = fw_cfg_dma_signature();

    uart_puts("[FB] fw_cfg signature: ");
    uart_puts(sig);
    uart_puts(" id=");
    fb_pr_hex32(fw_id);
    uart_puts(" dma=");
    fb_pr_hex64(dma_sig);
    uart_puts("\n");

    /* Trova il selector per etc/ramfb nel fw_cfg directory */
    uint16_t ramfb_sel = fw_cfg_find_file("etc/ramfb");
    if (ramfb_sel == 0) {
        /*
         * Con virtio-GPU il buffer locale del kernel viene esposto più tardi
         * tramite RESOURCE_ATTACH_BACKING, quindi l'assenza di ramfb non è
         * fatale: il framebuffer esiste comunque in RAM e verrà presentato
         * dal backend GPU attivo.
         */
        fb_clear(0x001a1a2e);
        fb_flush();
        uart_puts("[EnlilOS] Framebuffer locale pronto (ramfb assente, attendo backend GPU)\n");
        return;
    }

    /* Prepara la configurazione ramfb (big-endian) nel buffer statico */
    ramfb_cfg_t cfg;
    cfg.addr   = bswap64((uint64_t)(uintptr_t)framebuffer);
    cfg.fourcc = bswap32(0x34325258); /* XR24 = XRGB8888 */
    cfg.flags  = 0;
    cfg.width  = bswap32(FB_WIDTH);
    cfg.height = bswap32(FB_HEIGHT);
    cfg.stride = bswap32(FB_WIDTH * FB_BPP);

    uart_puts("[FB] ramfb selector=");
    fb_pr_hex32((uint32_t)ramfb_sel);
    uart_puts(" fb=");
    fb_pr_hex64((uint64_t)(uintptr_t)framebuffer);
    uart_puts(" stride=");
    fb_pr_hex32(FB_WIDTH * FB_BPP);
    uart_puts("\n");

    /* Scrivi tramite DMA fw_cfg (unica modalità affidabile su QEMU virt) */
    fw_cfg_dma_write(ramfb_sel, &cfg, sizeof(cfg));

    /* Pulisci lo schermo e scrivi in memoria fisica prima che QEMU legga */
    fb_clear(0x001a1a2e);
    fb_flush();

    uart_puts("[EnlilOS] Framebuffer inizializzato: 800x600x32\n");
}

uint32_t *fb_get_ptr(void)
{
    return (uint32_t *)fb_ptr;
}

void fb_flush(void)
{
    cache_flush_range((uintptr_t)framebuffer,
                      FB_WIDTH * FB_HEIGHT * FB_BPP);
}

void fb_clear(uint32_t color)
{
    color = fb_opaque(color);
    for (uint32_t i = 0; i < FB_WIDTH * FB_HEIGHT; i++) {
        fb_ptr[i] = color;
    }
}

void fb_put_pixel(uint32_t x, uint32_t y, uint32_t color)
{
    if (x < FB_WIDTH && y < FB_HEIGHT) {
        fb_ptr[y * FB_WIDTH + x] = fb_opaque(color);
    }
}

void fb_draw_char(uint32_t x, uint32_t y, char c, uint32_t fg, uint32_t bg)
{
    uint8_t ch = (uint8_t)c;
    int idx;

    if (ch < 32U || ch > 126U) {
        /* Carattere fuori range font: usa spazio (indice 0) */
        idx = 0;
    } else {
        idx = (int)ch - 32;
    }

    /* Limita all'array disponibile */
    if (idx < 0 || idx >= (int)(sizeof(font_8x16) / sizeof(font_8x16[0])))
        idx = 0;

    const uint8_t *glyph = font_8x16[idx];

    for (uint32_t row = 0; row < 16; row++) {
        uint8_t bits = glyph[row];
        for (uint32_t col = 0; col < 8; col++) {
            uint32_t color = (bits & (0x80 >> col)) ? fg : bg;
            fb_put_pixel(x + col, y + row, color);
        }
    }
}

void fb_draw_string(uint32_t x, uint32_t y, const char *s, uint32_t fg, uint32_t bg)
{
    fb_draw_string_utf8(x, y, s, fg, bg);
}

void fb_draw_string_centered(const char *s, uint32_t fg, uint32_t bg)
{
    uint32_t len = utf8_strlen(s);
    uint32_t text_width = len * 8;   /* 8 pixel per carattere */
    uint32_t text_height = 16;       /* 16 pixel per riga */

    uint32_t x = (FB_WIDTH - text_width) / 2;
    uint32_t y = (FB_HEIGHT - text_height) / 2;

    fb_draw_string(x, y, s, fg, bg);
}

